<?php

/**
 * Це використовується для перевірки, чи правильно запущено додаток.
 * Якщо константа "LiveCMS" не визначена, додаток не працює належним чином.
 */

if (!defined("LiveCMS")) {
    // Якщо константа "LiveCMS" не визначена, то додаток не працює правильно
    die("Помилка: додаток не запущено належним чином.");
}

/**
 * Встановлюємо HTTP-заголовок, який інформує, що сервер працює під управлінням "LiveCMS".
 * Цей заголовок корисний для інформування про те, яка платформа використовується.
 */

header('Powered: LiveCMS - Creative Resource Management');

/**
 * Вказуємо браузеру, що кешування дозволено для загального доступу.
 * Це може бути корисно для статичних ресурсів, щоб зменшити навантаження на сервер.
 */

header("Cache-control: public");

/**
 * Перевірка, чи існує файл livecms.php перед підключенням.
 * Це допомагає уникнути помилок, якщо файл не знайдений.
 */

$livecmsPath = $_SERVER['DOCUMENT_ROOT'] . '/platform/livecms.php';

if (!file_exists($livecmsPath)) {
    // Якщо файл не існує, вивести помилку і зупинити виконання
    die("Помилка: Файл 'livecms.php' не знайдений за шляхом: $livecmsPath");
}

/**
 * Підключаємо файл livecms.php, який містить важливі функції та налаштування для додатка.
 */

require $livecmsPath;

/**
 * Перевірка наявності файлу плагіна перед підключенням.
 * Це допомагає уникнути помилок, якщо плагін не знайдений.
 */

$pluginPath = ROOT . '/modules/example/index.php';

if (!file_exists($pluginPath)) {
    // Якщо плагін не знайдений, вивести помилку і зупинити виконання
    die("Помилка: Плагін 'example' не знайдений за шляхом: $pluginPath");
}

/**
 * Підключаємо плагін для тестування.
 * Це дозволяє підключати додаткові модулі чи плагіни в залежності від вимог.
 */

require $pluginPath;
